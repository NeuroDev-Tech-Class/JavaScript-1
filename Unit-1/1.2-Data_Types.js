//A data type is a classification of data that tells the computer how to interpret and use it.
// Here are the data types in JavaScript:

// Integers (int)
// These are whole numbers
console.log(42);
console.log(3 + 9);
console.log(-15);

// Floats (floating point number)
// These are numbers with a decimal point
console.log(3.1415);
console.log(10.7 * 0.5);
console.log(-17.3);

// Strings (str)
// These are text values made up of characters enclosed in quotes
console.log("Hello World!");
console.log('Th1s 1s c00l');
console.log("#$&*(@#$*&^");

// Boolean
// This a logical value that is either true or false
console.log(true);
console.log(false);
console.log(3 > 2);


// You can use the typeof function to check what data type something is
typeof "Hello";
typeof 3;
typeof 4.654;
typeof true;